package de.lere.vaad;

public class EndOfTheWorldException extends RuntimeException {

	public EndOfTheWorldException() {
		super();
	}

	public EndOfTheWorldException(String message, Throwable cause) {
		super(message, cause);
	}

	public EndOfTheWorldException(String message) {
		super(message);
	}

	public EndOfTheWorldException(Throwable cause) {
		super(cause);
	}

	
	
}
